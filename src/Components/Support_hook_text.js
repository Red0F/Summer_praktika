export const  TextList=({ items=data })=> {
// state для хранения id активного элемента, изначально null (ничего не открыто)
const [activeId, setActiveId] = useState(null);


    // обработчик клика по элементу
    const handleClick = (id) => {
        // если кликнули по уже открытому элементу — закрываем его, иначе открываем новый
        setActiveId(prevId => (prevId === id ? null : id));
    };

    return (
        <div>
        {items.map(({ id, title, hiddenText }) => (
            <div key={id} style={{ marginBottom: '40px', cursor: 'pointer', color: 'black' }}>
            <div onClick={() => handleClick(id)}>
                {title}
            </div>
            {activeId === id && (
                <div style={{ margin: '5px', color: 'black' }}>
                {Array.isArray(hiddenText) ? (
                    <ul>
                    {hiddenText.map((point, idx) => (
                        <li key={idx}>{point}</li>
                    ))}
                    </ul>
                ) : (
                    <div>{hiddenText}</div>
                )}
                </div>
            )}
            </div>
        ))}
        </div>
    );
}
